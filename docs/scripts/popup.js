function openPopup(url) {
  // Ouvre une nouvelle fenêtre avec une taille définie
  window.open(
    url,  // URL de la page de contact (peut être un autre fichier HTML)
      'popupWindow',    // Nom de la fenêtre
      'width=900,height=900,left=200,top=100' // Taille et position de la fenêtre
  );
}